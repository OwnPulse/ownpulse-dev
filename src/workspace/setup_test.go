func TestGenerateSettings_NewFile(t *testing.T) {
	dir := t.TempDir()
	if err := os.MkdirAll(filepath.Join(dir, ".claude"), 0755); err != nil {
		t.Fatal(err)
	}

	if err := generateSettings(dir, false); err != nil {
		t.Fatal(err)
	}

	data, err := os.ReadFile(filepath.Join(dir, ".claude", "settings.json"))
	if err != nil {
		t.Fatal(err)
	}

	var settings map[string]interface{}
	if err := json.Unmarshal(data, &settings); err != nil {
		t.Fatalf("invalid JSON: %v", err)
	}

	env, ok := settings["env"].(map[string]interface{})
	if !ok {
		t.Fatal("missing env key")
	}
	if env["CLAUDE_CODE_EXPERIMENTAL_AGENT_TEAMS"] != "1" {
		t.Errorf("teams flag = %v, want \"1\"", env["CLAUDE_CODE_EXPERIMENTAL_AGENT_TEAMS"])
	}
}

func TestGenerateSettings_MergesExisting(t *testing.T) {
	dir := t.TempDir()
	claudeDir := filepath.Join(dir, ".claude")
	if err := os.MkdirAll(claudeDir, 0755); err != nil {
		t.Fatal(err)
	}

	existing := `{
  "permissions": {"allow": ["Bash"]},
  "env": {"MY_VAR": "keep"}
}`
	if err := os.WriteFile(filepath.Join(claudeDir, "settings.json"), []byte(existing), 0644); err != nil {
		t.Fatal(err)
	}

	if err := generateSettings(dir, false); err != nil {
		t.Fatal(err)
	}

	data, err := os.ReadFile(filepath.Join(claudeDir, "settings.json"))
	if err != nil {
		t.Fatal(err)
	}

	var settings map[string]interface{}
	if err := json.Unmarshal(data, &settings); err != nil {
		t.Fatalf("invalid JSON: %v", err)
	}

	// Existing permissions preserved.
	perms, ok := settings["permissions"].(map[string]interface{})
	if !ok {
		t.Fatal("permissions key was clobbered")
	}
	if perms["allow"] == nil {
		t.Error("permissions.allow was clobbered")
	}

	// Existing env var preserved.
	env := settings["env"].(map[string]interface{})
	if env["MY_VAR"] != "keep" {
		t.Errorf("MY_VAR = %v, want \"keep\"", env["MY_VAR"])
	}

	// Teams flag added.
	if env["CLAUDE_CODE_EXPERIMENTAL_AGENT_TEAMS"] != "1" {
		t.Errorf("teams flag = %v, want \"1\"", env["CLAUDE_CODE_EXPERIMENTAL_AGENT_TEAMS"])
	}
}

