func TestGenerateCLAUDEmd_Overwrites(t *testing.T) {
	dir := t.TempDir()
	claudeDir := filepath.Join(dir, ".claude")
	if err := os.MkdirAll(claudeDir, 0755); err != nil {
		t.Fatal(err)
	}

	path := filepath.Join(claudeDir, "CLAUDE.md")
	if err := os.WriteFile(path, []byte("old content"), 0644); err != nil {
		t.Fatal(err)
	}

	agentsDir := testAgentsDir(t)

	cfg := &config.WorkspaceConfig{
		Repos: []config.RepoConfig{{Name: "app", Description: "Main app"}},
	}
	repo := config.RepoConfig{Name: "app"}

	if err := generateCLAUDEmd(cfg, repo, dir, agentsDir, false); err != nil {
		t.Fatal(err)
	}

	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatal(err)
	}
	if strings.Contains(string(data), "old content") {
		t.Error("CLAUDE.md was not overwritten")
	}
	if !strings.Contains(string(data), "Generated by opdev setup") {
		t.Error("CLAUDE.md missing generated header")
	}
}

